proc getMaxHeight(): int =
    result = 0
    for _, value in countries:
        if value.len > result:
            result = value.len

proc getMaxLength(): int =
    result = 0
    for _, value in countries:
        var valueMaxLenght = 0
        for line in value:
            if line.len > valueMaxLenght:
                valueMaxLenght = line.len
        if valueMaxLenght > result:
            result = valueMaxLenght

const countriesMaxHeight* = getMaxHeight()
const countriesMaxLength* = getMaxLength()